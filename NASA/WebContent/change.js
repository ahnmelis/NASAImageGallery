/**
 * changing scope and visibility
 * 
 */

function changeScope(elemToShow,elemToHide) {
	    	document.getElementById(elemToShow).style.display = "block";
	    	document.getElementById(elemToHide).style.display = "none";
		}
		
		function changeVisibility(elemToShow,elemToHide) {
    		document.getElementById(elemToShow).style.visibility = "visible";
    		document.getElementById(elemToHide).style.visibility = "hidden";
		}